"""
Phase 6-PT2 tests for core/reporting/submission_render.py.

The critical correctness properties this file pins:

  1. NONE of the banned scanner-output phrases appear in the rendered
     markdown. The list is in submission_render._BANNED_PHRASES. If
     any of these escape into a submitted report, the triager flags
     it as automated tool output.

  2. The title is impact-focused (not class-jargon-focused). "Cross-
     Principal IDOR (active verification)" never appears as a title;
     "Any authenticated user can read any other user's ..." does.

  3. The TL;DR is researcher-narrative voice. First-person observation,
     concrete impact, prerequisite stated. No weasel hedging.

  4. The repro steps come from the VC3 output when provided —
     operator-verified always wins over heuristic.

  5. There's no Sentinel branding in the output (no "Generated by",
     no scan ID, no tool name).
"""
from __future__ import annotations

from dataclasses import dataclass, field
from typing import Any, Dict, List, Optional

import pytest

from core.reporting.submission_render import (
    _BANNED_PHRASES,
    _canonicalize_vuln_class,
    _strip_banned_phrases,
    render_for_submission,
)


@dataclass
class _FakeReport:
    """Stand-in for BountyReport. Only the fields render_for_submission
    actually reads."""
    title: str = "Finding"
    summary: str = ""
    type: str = ""
    severity: str = "HIGH"
    target: str = ""
    asset: str = ""
    steps_to_reproduce: List[str] = field(default_factory=list)
    evidence: List[str] = field(default_factory=list)


# ───────────────────────── banned-phrase stripping ─────────────────────────


class TestBannedPhrases:
    def test_every_banned_phrase_is_stripped(self):
        for phrase in _BANNED_PHRASES:
            text = f"prelude. {phrase}. postlude."
            stripped = _strip_banned_phrases(text)
            assert phrase not in stripped.lower(), (
                f"banned phrase {phrase!r} survived stripping"
            )

    def test_case_insensitive(self):
        text = "A high severity vulnerability MAY BE PRESENT here."
        stripped = _strip_banned_phrases(text)
        assert "may be present" not in stripped.lower()
        assert "MAY BE PRESENT" not in stripped

    def test_double_space_cleaned(self):
        """When we strip a phrase mid-sentence, the leftover spaces
        get cleaned up so output reads naturally."""
        stripped = _strip_banned_phrases("foo may be present bar")
        # No double-spaces in output.
        assert "  " not in stripped


