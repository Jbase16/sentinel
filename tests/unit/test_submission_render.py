# ───────────────────────── title rewriting ─────────────────────────


class TestTitleRewriting:
    def test_idor_title_is_impact_focused(self):
        r = _FakeReport(
            title="[High] Cross-Principal IDOR (active verification) in /api/users/42",
            target="https://api.example.com/api/users/42",
            asset="https://api.example.com/api/users/42",
        )
        rendered = render_for_submission(r)
        # Title says what the bug DOES, not what it's CALLED.
        assert "any authenticated user can read" in rendered.title.lower()
        # And mentions the host so the triager can match scope.
        assert "api.example.com" in rendered.title

    def test_title_strips_active_verification_parenthetical(self):
        """No internal Sentinel labels in the output."""
        r = _FakeReport(
            title="[High] Cross-Principal IDOR (active verification)",
            target="https://app.example/api/foo/1",
            asset="https://app.example/api/foo/1",
        )
        rendered = render_for_submission(r)
        assert "(active verification)" not in rendered.markdown
        assert "(active verification)" not in rendered.title


# ───────────────────────── TL;DR voice ─────────────────────────


class TestTLDRVoice:
    def test_idor_tldr_uses_researcher_voice(self):
        r = _FakeReport(
            title="Cross-Principal IDOR",
            target="https://api.example.com/api/users/42",
            asset="https://api.example.com/api/users/42",
            evidence=['{"id":42,"email":"victim@example.com","role":"user"}'],
        )
        rendered = render_for_submission(r)
        # First-person observation.
        assert "while reviewing" in rendered.tldr.lower() or \
               "i found" in rendered.tldr.lower() or \
               "i noticed" in rendered.tldr.lower()
        # Concrete impact mentions the target host.
        assert "api.example.com" in rendered.tldr.lower()
        # And surfaces real data field names from the evidence.
        assert "`email`" in rendered.tldr or "`id`" in rendered.tldr

    def test_tldr_avoids_weasel_phrasing(self):
        r = _FakeReport(
            title="IDOR",
            summary="A high severity vulnerability may be present in /api/users/42",
            target="https://example.com/api/users/42",
            asset="https://example.com/api/users/42",
        )
        rendered = render_for_submission(r)
        for weasel in (
            "may be present", "may affect", "could allow",
            "depending on the application context",
        ):
            assert weasel not in rendered.markdown.lower(), (
                f"weasel phrase {weasel!r} leaked into submission markdown"
            )


# ───────────────────────── repro steps ─────────────────────────


class TestRepoSteps:
    def test_verify_steps_replace_heuristic_steps(self):
        """When operator provides VC3-generated repro, that wins —
        the BountyReport's heuristic steps are ignored."""
        verify_steps = [
            "As user `alice`, send `GET /api/users/2` — server returns HTTP 200 with bob's data.",
        ]
        r = _FakeReport(
            title="IDOR",
            target="http://h/api/users/2",
            asset="http://h/api/users/2",
            steps_to_reproduce=["DO NOT USE - heuristic step"],  # should NOT appear
        )
        rendered = render_for_submission(r, verify_steps=verify_steps)
        assert "DO NOT USE" not in rendered.markdown
        assert "As user `alice`" in rendered.markdown

    def test_no_verify_steps_falls_back_to_report_steps(self):
        r = _FakeReport(
            title="IDOR",
            target="http://h/api/users/2",
            asset="http://h/api/users/2",
            steps_to_reproduce=["Step 1: do X.", "Step 2: do Y."],
        )
        rendered = render_for_submission(r)  # no verify_steps arg
        assert "Step 1: do X." in rendered.markdown


