# core/__init__.py
# Package Marker for Python Core Backend
# ============================================================================
#
# PURPOSE:
# This file tells Python that the "core" directory is a package (a collection
# of related modules). Think of it like a table of contents for the backend.
#
# WHY IT'S EMPTY:
# We don't need any code here - just having this file present tells Python:
# "You can import modules from the core/ directory like: from core.ai import..."
#
# PYTHON PACKAGE BASICS:
# - A directory with __init__.py becomes importable
# - Without this file, Python wouldn't recognize "core" as a module
# - We could add shared initialization code here, but we keep it minimal
#
